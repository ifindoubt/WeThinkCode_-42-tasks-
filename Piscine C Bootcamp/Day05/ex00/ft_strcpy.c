#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	a;

	a = 0;
	while (src[a] > 0)
	{
		src[a] = dest[a];
		a++;
	}
	if (src[a] < 0)
	{
		return (0);
	}
	return (dest);
}

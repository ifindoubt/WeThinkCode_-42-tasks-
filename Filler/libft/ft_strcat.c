#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t len;
	size_t a;

	len = ft_strlen(s1);
	a = 0;
	while (s2[a] != '\0')
	{
		s1[len + a] = s2[a];
		a++;
	}
	s1[len + a] = '\0';
	return (s1);
}

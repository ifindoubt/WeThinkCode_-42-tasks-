#include "libft.h"

int		ft_isalpha(int c)
{
	while ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
	{
		if (c)
			return (1);
		c++;
	}
	if (!(c))
		return (0);
	c++;
	return (0);
}

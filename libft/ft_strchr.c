#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*ss;

	ss = (char *)s;
	while (*ss != c)
	{
		if (!*ss)
		{
			return (0);
		}
		ss++;
	}
	return (ss);
}
